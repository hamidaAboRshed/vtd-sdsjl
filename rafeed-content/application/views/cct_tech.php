        <h3>
          Warm White
        </h3>
      </div>
      <p>
        This refers commonly incandescent bulbs (and now LED) and are typically in the
        2,700K-3,500K color temperature range. Many historical Warm White bulbs are standard
        filament based incandescents (although CFL and LED have certainly produced acceptable
        counterparts). Warm White definitely has a more relaxed feel and can even "appear" dimmer
        than higher/cooler color temperatures.
      </p>
    </div>
    <div class="info-block">
      <div class="info-title">
        <div id="natural_white"></div>
        <h3>
          Natural White
        </h3>
      </div>
      <p>
        This refers to bulbs (frequently fluorescent styles) that are in the 4,100K range. This color temperature emits a much more white form of light, but does not venture into the blue range yet.
        Because it is much less orange than Warm White, it can stimulate more than sedate and is
        used frequently in offices and warehouses with 4' fluorescent or LED tubes.
      </p>
    </div>
    <div class="info-block">
      <div class="info-title">
        <div id="cool_white"></div>
        <h3>
          Cool White
        </h3>
      </div>
      <p>
        This refers to bulbs with temperatures that circle around the 6,000K-6,500K mark (also com
        monly called "Full Spectrum"). This has received the name "Daylight" due to the tendency for
        6,000K-6,500K range to reflect what the color temperature of the sun produces at 12:00 noon
        on a cloudless day. Because of the sunlight emulation effect, there are many people in north
        ern climates that love these color temperatures as it can assist in coping with Seasonal Affec
        tive Disorder.
      </p>
